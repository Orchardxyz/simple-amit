export type I18nKey = keyof (typeof messages)["en"];
export type Translator = (key: I18nKey, params?: I18nParams) => string;

export function createTranslator(language: UiLanguage): Translator {
  const languageMessages = messages[language];

  return (key, params = {}) => {
    let message: string = languageMessages[key];

    for (const [name, value] of Object.entries(params)) {
      message = message.replaceAll(`{${name}}`, String(value));
    }

    return message;
  };
}
